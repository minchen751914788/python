# 6、 Python的解释器如今有多个语言实现，我们常用的是CPython（官方版本的C语言实现），7、其他还有Jython（可以运行在Java平台）、IronPython（可以运行在.NET和Mono平台）、PyPy（Python实现的，支持JIT即时编译）
